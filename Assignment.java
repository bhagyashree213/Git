class Assignment
{
    public static void main(String[] args)
    {
        int a = 10;
        System.out.println("a => " + a); // 10

        a += 5; // a = a + 5
        System.out.println("a => " + a); // 15

        a *= 2; // a = a * 2
        System.out.println("a => " + a); // 30

        a /= 6; // a = a / 6
        System.out.println("a => " + a); // 5

        a -= 2; // a = a - 2
        System.out.println("a => " + a); // 3
    }
}
